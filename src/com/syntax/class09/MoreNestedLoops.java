package com.syntax.class09;

public class MoreNestedLoops {

	public static void main(String[] args) {
		// How to create a multiplication table
		
		for(int i=1; i<=10; i++) {
			
			for(int x=1; x<=10; x++) {
				System.out.print(i+" * "+x+" = "+(i*x)+"     ");
			}
		}

		
		
		
		
		
		
		
		
		
		
	}

}
