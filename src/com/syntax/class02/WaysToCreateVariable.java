package com.syntax.class02;

public class WaysToCreateVariable {

	public static void main(String[] args) {
		// remember to create a variable we need 2 things: datatype and name
		
		/*
		 * 1 way to create a variable:
		 * Data Type name=value;
		 */
		int temperature =60;//declared a variable: temperature and we initialized it(assigned value)
		
		/*
		 * Second way this is done is in 2 steps:
		 * create a variable (data type and name)
		 * Assign the value
		 */
		
		int sum;  //empty box with no value but created a variable or declared a variable
		sum=30; // Assigned the value
		sum=50; //replacing value 30 with value 50 (reassigned the value)
		
		int num1, num2, num3;
		num1=10; // Assigned the value
		num2=20; // Assigned the value
		num3=30; // Assigned the value
		
		System.out.println(sum);  //50
		
		
		//Task
		
		byte age=33;
		
		age=34;
		
		short year;
		year= 2022;
		
		year=2023;
		
		int zipcode=37686;
		
		zipcode=37615;
		
		long phone;
		phone=4237477753L;
		
		phone=8173004577L;
		
		float price= 4.99F;
		
		price=8.99F;
		
		double pi;
		pi=3.14;
		
		pi=3.14159;
		
		char gender;
		gender='M';
		
		gender='F';
		
		boolean sick=true;
		
		sick=false;
		
		System.out.println(age);
		System.out.println(year);
		System.out.println(zipcode);
		System.out.println(phone);
		System.out.println(price);
		System.out.println(pi);
		System.out.println(gender);
		System.out.println(sick);
		
		
				
				
		
		
		

	}

}
